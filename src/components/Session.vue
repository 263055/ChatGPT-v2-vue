<template>
  <div :class="{ activeCard: sessionInfo.id === current }" class="person-card">
    <div class="info">
      <div class="info-detail">
        <div class="name">{{ sessionInfo.name }}</div>
      </div>
    </div>
  </div>
</template>

<script>

export default {
  props: {
    sessionInfo: {
      default: {},
    },
    pcCurrent: {
      default: ''
    }
  },
  data() {
    return {
      current: '',
    }
  },
  watch: {
    pcCurrent: function () {
      this.isActive()
    }
  },
  methods: {
    isActive() {
      this.current = this.pcCurrent
    },
    truncateString(str, num) {
      if (str.length <= num) {
        return str;
      }
      return str.slice(0, num) + "...";
    },
  }
};
</script>

